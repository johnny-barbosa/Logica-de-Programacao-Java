package logica.programacao;

public class Aula11_OperadoresLogicos {
    public static void main(String[] args) {

        boolean x = true;
        boolean y = false;

        //&&: E lógico
        System.out.println( x && y);

        //||: Ou lógico
        System.out.println(x || y);

        //!Negacao lógica
        System.out.println(!x);
        System.out.println(!y);

    }
}
