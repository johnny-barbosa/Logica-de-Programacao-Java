package logica.programacao;

public class Aula5_6_Variaveis_TiposPrimitivos {
    public static void main(String[] args) {

        long numeroLongo = 1000000000000000000L;
        int idade = 25;
        double salario = 4500.00;
        char genero = 'm';
        boolean empregadoJava = true;

        System.out.println("numero longo: " + numeroLongo);
        System.out.println("idade: " + idade);
        System.out.println("Salario: "+ salario);
        System.out.println("Genero (m = masculino / f = feminino): " + genero);
        System.out.println("Empregado Java: "+ empregadoJava);


    }
}
